def run_command(
    cmd: Sequence[str],
    cwd: Path | None = None,
    check: bool = True,
    stream_output: bool = False,
) -> subprocess.CompletedProcess[str]:
    """
    Run a command and return the result.
    """
    print(f"Running: {' '.join(cmd)}")

    if cwd:
        print(f"  in: {cwd}")

    if stream_output:
        result = subprocess.run(cmd, cwd=cwd, text=True, check=False)
    else:
        result = subprocess.run(cmd, cwd=cwd, capture_output=True, text=True, check=False)

    if check and result.returncode != 0:
        if not stream_output:
            print(f"Error: {result.stderr}")
        raise subprocess.CalledProcessError(result.returncode, cmd)

    return result


def load_pyproject() -> dict:
    """
    Load the pyproject.toml located next to this script.
    """
    pyproject_path = Path(__file__).parent / "pyproject.toml"
    with pyproject_path.open("rb") as fp:
        return tomllib.load(fp)


def resolve_python_stub_root(pyproject: dict) -> Path:
    """
    Return the directory where pyo3-stub-gen writes .pyi files.
    """
    python_source = pyproject.get("tool", {}).get("maturin", {}).get("python-source", ".")

    dest_dir = (Path(__file__).parent / python_source).resolve()
    python_dir = Path(__file__).parent.resolve()
    try:
        dest_dir.relative_to(python_dir)
    except ValueError as e:
        raise RuntimeError(
            "python-source must stay within the python/ package directory",
        ) from e

    dest_dir.mkdir(parents=True, exist_ok=True)
    return dest_dir


def generate_stubs() -> bool:
    """
    Generate type stubs using pyo3-stub-gen.
    """
    print("Generating type stubs with pyo3-stub-gen...")

    crates_dir = Path(__file__).parent.parent / "crates" / "pyo3"
    pyproject = load_pyproject()
    dest_dir = resolve_python_stub_root(pyproject)
    module_name = pyproject.get("tool", {}).get("maturin", {}).get("module-name") or pyproject.get(
        "project",
        {},
    ).get("name", "nautilus_trader")
    module_root = module_name.split(".")[0]

    maturin_features = pyproject.get("tool", {}).get("maturin", {}).get("features", [])
    cargo_features = [f for f in maturin_features if f != "extension-module"]

    cmd = ["cargo", "run", "--bin", "python-stub-gen"]

    if cargo_features:
        cmd.extend(["--features", ",".join(cargo_features)])

    result = run_command(cmd, cwd=crates_dir, stream_output=True)

    print("Stubs generated successfully")

    if result.stdout:
        print(f"Output: {result.stdout}")

    stub_files = sorted(dest_dir.rglob("*.pyi"))

    if not stub_files:
        print("Stub generation completed but no .pyi files were produced")
        return False

    # Post-process all stub files
    workspace_root = Path(__file__).parent.parent
    root = dest_dir / "nautilus_trader"
    if root.exists():
        post_process_stubs(root)
        relocate_classes_from_libnautilus(root)
        inject_module_constants(root, workspace_root)
        format_stub_files(root)

    relative_root = dest_dir.relative_to(Path(__file__).parent)
    print(f"Type stubs written to {relative_root or Path('.')} ")

    # Refresh file list after processing
    stub_files = sorted(dest_dir.rglob("*.pyi"))
    preview_limit = 20
    filtered = [
        stub_file
        for stub_file in stub_files
        if stub_file.relative_to(dest_dir).parts
        and stub_file.relative_to(dest_dir).parts[0] == module_root
    ]

    targets = filtered if filtered else stub_files

    for stub_file in targets[:preview_limit]:
        print(f"Generated: {stub_file.relative_to(dest_dir)}")

    remaining = len(targets) - preview_limit
    if remaining > 0:
        print(f"...and {remaining} more stub files")

    return True


def post_process_stubs(root: Path) -> None:
    """Post-process all stub files: fix headers, rename methods, fix return types."""
    workspace_root = Path(__file__).parent.parent
    rust_fixups = collect_rust_class_fixups(workspace_root)
    renamed_enums = collect_renamed_enums(workspace_root)

    for stub_file in root.rglob("*.pyi"):
        content = stub_file.read_text()
        original = content

        # Ensure proper header with D401 ignore
        content = fix_stub_header(content)

        # Rename enum variants to match PyO3 rename_all = "SCREAMING_SNAKE_CASE"
        content = rename_enum_variants(content, renamed_enums)

        # Rename methods (py_new -> __init__, etc.)
        content = rename_methods(content)

        # Restore getter/staticmethod semantics from Rust bindings metadata
        content = apply_rust_class_fixups(content, rust_fixups)

        # Rename wrapper classes to their public Python names
        content = rename_stub_classes(content, rust_fixups)

        # Escape keywords introduced by fixup renames (e.g. py_from -> from)
        content = _escape_keyword_methods(content)

        # Strip docstrings (runtime __doc__ from PyO3 is the single source)
        content = strip_docstrings(content)

        # Remove imports extracted from doc comment examples
        content = remove_docstring_imports(content)

        # Add = None default to Optional parameters that lack one
        content = add_optional_defaults(content)

        # Restore actual default values from #[pyo3(signature)] attributes
        content = apply_signature_defaults(content, rust_fixups)

        # Fix enum variant names in default values (signature defaults use Rust names)
        content = fix_enum_defaults_in_signatures(content, renamed_enums)

        # Replace forward local class defaults that are invalid inside class bodies
        content = elide_forward_class_defaults_in_signatures(content)

        # Import model symbols used without a module qualifier
        content = add_missing_model_imports(content)

        # Normalize formatting
        content = normalize_stub_content(content)

        if content != original:
            stub_file.write_text(content)


