    /// Checks position consistency between cache and venue.
    ///
    /// This method validates that positions in the cache match the venue's state,
    /// detecting position drift and querying for missing fills when discrepancies
    /// are found.
    ///
    /// # Returns
    ///
    /// A vector of fill events generated to reconcile position discrepancies.
    pub async fn check_positions_consistency(
        &mut self,
        clients: &[&dyn ExecutionClient],
    ) -> Vec<OrderEventAny> {
        let check = self.prepare_position_report_check(UUID4::new());
        let mut reports = Vec::new();
        let mut failed_venues = IndexSet::new();

        for client in clients {
            match client
                .generate_position_status_reports(&check.command)
                .await
            {
                Ok(client_reports) => {
                    reports.extend(client_reports);
                }
                Err(e) => {
                    failed_venues.insert(client.venue());
                    log::warn!(
                        "Failed to query position reports from {}: {e}",
                        client.client_id()
                    );
                }
            }
        }

        self.reconcile_position_reports(&check, reports, &failed_venues)
    }

    /// Reconciles cached positions against venue position reports.
    #[must_use]
    pub(crate) fn reconcile_position_reports(
        &mut self,
        check: &PositionReportCheck,
        reports: Vec<PositionStatusReport>,
        failed_venues: &IndexSet<Venue>,
    ) -> Vec<OrderEventAny> {
        log::debug!("Checking position consistency between cached-state and venues");

        let mut venue_positions = IndexMap::new();

        for report in reports {
            if !self.should_reconcile_instrument(&report.instrument_id) {
                continue;
            }

            venue_positions.insert((report.instrument_id, report.account_id), report);
        }

        let mut events = Vec::new();

        for (key, cached_positions) in &check.positions_by_key {
            let venue_report = venue_positions.get(key);

            if venue_report.is_none()
                && Self::position_query_failed_for_instrument(failed_venues, &key.0)
            {
                log::warn!(
                    "Skipping position reconciliation for {}: failed to query venue position status",
                    key.0
                );
                continue;
            }

            if let Some(discrepancy_events) =
                self.check_position_discrepancy(*key, cached_positions, venue_report)
            {
                events.extend(discrepancy_events);
            }
        }

        for (key, venue_report) in &venue_positions {
            if check.positions_by_key.contains_key(key)
                || venue_report.signed_decimal_qty == Decimal::ZERO
            {
                continue;
            }

            if let Some(discrepancy_events) =
                self.check_position_discrepancy(*key, &[], Some(venue_report))
            {
                events.extend(discrepancy_events);
            }
        }

        // Prune retry counters for (instrument, account) pairs no longer actively
        // tracked, excluding flat venue reports which shouldn't protect stale counters
        let active_keys: IndexSet<InstrumentAccountKey> = check
            .positions_by_key
            .keys()
            .copied()
            .chain(
                venue_positions
                    .iter()
                    .filter(|(_, r)| r.signed_decimal_qty != Decimal::ZERO)
                    .map(|(k, _)| *k),
            )
            .collect();
        self.position_recon_retries
            .retain(|k, _| active_keys.contains(k));

        events
    }

    fn position_query_failed_for_instrument(
        failed_venues: &IndexSet<Venue>,
        instrument_id: &InstrumentId,
    ) -> bool {
        failed_venues.contains(&instrument_id.venue)
    }

    fn position_snapshot_avg_px(cached_positions: &[Position]) -> Option<Decimal> {
        let mut total_value = Decimal::ZERO;
        let mut total_qty = Decimal::ZERO;

        for position in cached_positions {
            let qty = position.signed_decimal_qty().abs();
            if position.avg_px_open > 0.0
                && qty > Decimal::ZERO
                && let Ok(avg_px) = Decimal::from_str(&position.avg_px_open.to_string())
            {
                total_value += avg_px * qty;
                total_qty += qty;
            }
        }

        if total_qty > Decimal::ZERO {
            Some(total_value / total_qty)
        } else {
            None
        }
    }

    /// Registers an order as inflight for tracking.
    pub fn register_inflight(&mut self, client_order_id: ClientOrderId) {
        let ts_submitted = self.clock.borrow().timestamp_ns();
        self.inflight_checks.insert(
            client_order_id,
            InflightCheck {
                client_order_id,
                ts_submitted,
                retry_count: 0,
                last_query_ts: None,
            },
        );
        self.recon_check_retries.insert(client_order_id, 0);
        self.ts_last_query.shift_remove(&client_order_id);
        self.order_local_activity_ns.shift_remove(&client_order_id);
    }

    /// Records local activity for the specified order.
    ///
    /// Uses the current clock time (receipt time) instead of venue time to accurately
    /// track when we last processed activity for this order. This avoids race conditions
    /// where network/queue latency makes events appear "old" even though they just arrived.
    pub fn record_local_activity(&mut self, client_order_id: ClientOrderId) {
        let ts_now = self.clock.borrow().timestamp_ns();
        self.order_local_activity_ns.insert(client_order_id, ts_now);
    }

    /// Clears reconciliation tracking state for an order.
    pub fn clear_recon_tracking(&mut self, client_order_id: &ClientOrderId, drop_last_query: bool) {
        self.inflight_checks.shift_remove(client_order_id);
        self.recon_check_retries.shift_remove(client_order_id);

        if drop_last_query {
            self.ts_last_query.shift_remove(client_order_id);
        }
        self.order_local_activity_ns.shift_remove(client_order_id);
    }

    /// Returns any external order claim for the given instrument ID.
    #[must_use]
    pub fn get_external_order_claim(&self, instrument_id: &InstrumentId) -> Option<StrategyId> {
        self.external_order_claims.get(instrument_id).copied()
    }

    /// Claims external orders for a specific strategy and instrument.
    ///
    /// # Errors
    ///
    /// Returns an error if the instrument already has a registered claim.
    pub fn claim_external_orders(
        &mut self,
        instrument_id: InstrumentId,
        strategy_id: StrategyId,
    ) -> anyhow::Result<()> {
        if let Some(existing) = self.external_order_claims.get(&instrument_id) {
            anyhow::bail!("External order claim for {instrument_id} already exists for {existing}");
        }

        self.external_order_claims
            .insert(instrument_id, strategy_id);
        Ok(())
    }

    /// Records position activity for reconciliation tracking, scoped per (instrument, account).
    ///
    /// The activity is stamped from the monotonic `dst::time` clock (real elapsed
    /// time), **not** from `self.clock` and **not** from the venue event's
    /// `ts_event`. The position-discrepancy grace is a real-time settling window:
    /// "give the local pipeline a moment to catch up before flagging a
    /// cache-vs-venue gap". That is inherently wall/monotonic time — you want N
    /// real seconds of cover regardless of the trading clock's epoch or speed.
    /// `self.clock` can be driven off wall time (e.g. an accelerated simulated
    /// venue), which would shrink the window by the clock's speed; the venue
    /// `ts_event` lives on yet another axis. Measuring against the same monotonic
    /// clock the reconciliation loop already schedules on keeps the grace honest.
    /// See `check_position_discrepancy`.
    pub fn record_position_activity(&mut self, instrument_id: InstrumentId, account_id: AccountId) {
        self.position_local_activity
            .insert((instrument_id, account_id), dst::time::Instant::now());
    }

    /// Returns the current position-reconciliation retry count for the given
    /// `(instrument, account)` key, or zero if no entry exists.
    #[must_use]
    pub fn position_recon_retry_count(&self, key: &InstrumentAccountKey) -> u32 {
        self.position_recon_retries.get(key).copied().unwrap_or(0)
    }

    /// Observes an incoming execution report and updates tracking state.
    ///
    /// This should be called **before** the report is dispatched to the execution
    /// engine, so that the manager's state is current when periodic checks run.
    ///
    /// Updates performed per report variant:
    /// - `Order`: clears inflight tracking and records local activity
    /// - `Fill`: marks fill as processed, records order and position activity
    /// - `OrderWithFills`: clears inflight tracking, records local activity, records position activity per fill
    /// - `Position`: records position activity
    /// - `MassStatus`: no-op (handled separately via startup reconciliation)
    pub fn observe_execution_report(&mut self, report: &ExecutionReport) {
        match report {
            ExecutionReport::Order(order_report) => {
                if let Some(client_order_id) = &order_report.client_order_id {
                    // Only clear inflight tracking for non-pending states.
                    // Pending reports (PendingUpdate, PendingCancel) are interim
                    // acknowledgements; the order is still inflight until the
                    // venue confirms the final state.
                    if !matches!(
                        order_report.order_status,
                        OrderStatus::PendingUpdate | OrderStatus::PendingCancel
                    ) {
                        self.clear_recon_tracking(
                            client_order_id,
                            order_report.order_status.is_closed(),
                        );
                    }
                    self.record_local_activity(*client_order_id);
                }
            }
            ExecutionReport::Fill(fill_report) => {
                let client_order_id = fill_report.client_order_id.or_else(|| {
                    self.cache
                        .borrow()
                        .client_order_id(&fill_report.venue_order_id)
                        .copied()
                });

                if let Some(coid) = client_order_id {
                    self.record_local_activity(coid);
                }
                self.record_position_activity(fill_report.instrument_id, fill_report.account_id);
            }
            ExecutionReport::OrderWithFills(order_report, fills) => {
                if let Some(client_order_id) = &order_report.client_order_id
                    && !matches!(
                        order_report.order_status,
                        OrderStatus::PendingUpdate | OrderStatus::PendingCancel
                    )
                {
                    self.clear_recon_tracking(
                        client_order_id,
                        order_report.order_status.is_closed(),
                    );
                    self.record_local_activity(*client_order_id);
                }

                for fill_report in fills {
                    self.record_position_activity(
                        fill_report.instrument_id,
                        fill_report.account_id,
                    );
                }
            }
            ExecutionReport::Position(position_report) => {
                self.record_position_activity(
                    position_report.instrument_id,
                    position_report.account_id,
                );
            }
            ExecutionReport::MassStatus(_) => {
                // Handled separately via reconcile_execution_mass_status
            }
        }
    }

    /// Checks if a fill has been recently processed (for deduplication).
    #[must_use]
    pub fn is_fill_recently_processed(&self, trade_id: &TradeId) -> bool {
        self.recent_fills_cache.contains_key(trade_id)
    }

    /// Marks a fill as recently processed with current timestamp.
    pub fn mark_fill_processed(&mut self, trade_id: TradeId) {
        let ts_now = self.clock.borrow().timestamp_ns();
        self.recent_fills_cache.insert(trade_id, ts_now);
    }

    /// Prunes expired fills from the recent fills cache.
    ///
    /// Default TTL is 60 seconds.
    #[expect(
        clippy::cast_precision_loss,
        clippy::cast_possible_truncation,
        clippy::cast_sign_loss,
        reason = "TTL is a small positive seconds value; the nanosecond product fits u64"
    )]
    pub fn prune_recent_fills_cache(&mut self, ttl_secs: f64) {
        let ts_now = self.clock.borrow().timestamp_ns();
        let ttl_ns = (ttl_secs * NANOSECONDS_IN_SECOND as f64) as u64;

        self.recent_fills_cache
            .retain(|_, &mut ts_cached| ts_now - ts_cached <= ttl_ns);
    }

